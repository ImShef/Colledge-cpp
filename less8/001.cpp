#include <iostream>

int main() {
    std::cout << "\033[32m" << "Это зеленый текст." << "\n";
    std::cout << "\033[44m" << "Это синий фон." << "\n";
    std::cout << "\033[31;47m" << "Это красный текст на белом фоне." << "\n";
    std::cout << "\033[0m" << "Это сброс к стандартным настройкам." << "\n";
    
    return 0;
}
